  @doc """
  Отримати набір налаштувань для конкретного ідентифікатора контролю.
  Наприклад: `ERPUNO.Profile.get_control("AC-16")`
  """
  def get_control(id) do
    Enum.find(controls(), fn c -> c.id == id end)
  end

  @doc """
  Отримати всі параметри конфігурації для вказаної бібліотеки.
  Наприклад: `ERPUNO.Profile.get_library_config("synrc/kvs")`
  """
  def get_library_config() do
    controls()
    |> Enum.flat_map(fn c -> c.subcontrols end)
    |> Enum.map(fn sc -> {sc.id, sc.parameters} end)
  end

  def get_library_config(lib_name) do
    controls()
    |> Enum.filter(fn c -> c.library == lib_name end)
    |> Enum.flat_map(fn c -> c.subcontrols end)
    |> Enum.map(fn sc -> {sc.id, sc.parameters} end)
  end

  @doc """
  Форматування конфігурації у формат для генерації документації (Markdown).
  """
  def generate_md do
    controls()
    |> Enum.map(fn c ->
      subcontrols_docs =
        Enum.map(c.subcontrols, fn sc ->
          params =
            Enum.map(sc.parameters, fn p ->
              "- `#{p.name}` (Тип: #{inspect(p.type)}, За замовчуванням: **#{inspect(p.default)}**)"
            end)
            |> Enum.join("\n")

          """
          ### #{sc.id}: #{sc.name}
          **Реалізація:** #{sc.text}

          **Параметри налаштування:**
          #{params}
          """
        end)
        |> Enum.join("\n")

      """
      # #{c.id}: #{c.name}
      **Компонент:** `#{c.library}`
      **OID:** `#{inspect(c.oid)}`

      **Опис сімейства:**
      #{c.text}

      ## Підконтрольні елементи
      #{subcontrols_docs}
      """
    end)
    |> Enum.join("\n\n---\n\n")
  end
