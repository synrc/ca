defmodule CA.L2.CourtTech do
  @moduledoc """
  Level 2: Sectoral Technical Security Profile (Галузевий технічний профіль безпеки Судової Системи)

  Містить суто технічні заходи безпеки з профілю L2 Court, що потребують
  модифікації коду або налаштування технічних параметрів системи та засобів безпеки.
  """

  def controls do
    CA.L2.Court.controls()
    |> Enum.filter(&CA.L2.Court.technical?/1)
  end
end

defmodule CA.L2.CourtOrg do
  @moduledoc """
  Level 2: Sectoral Organizational Security Profile (Галузевий організаційний профіль безпеки Судової Системи)

  Чисто організаційний профіль безпеки, де руками здійснюється ручний моніторинг,
  аудит, лекції з обізнаності (AT), контроль кадрової безпеки, фізичного доступу тощо.
  """

  def controls do
