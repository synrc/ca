  def get_children_text(family_id, all_specs) do
    family_prefix = to_string(family_id) <> "-"
    
    children_titles = 
      all_specs
      |> Enum.filter(fn spec -> 
        str_id = to_string(spec.id)
        String.starts_with?(str_id, family_prefix) and str_id != to_string(family_id)
      end)
      |> Enum.sort_by(fn spec -> 
        spec.id 
        |> to_string() 
        |> String.split("-") 
        |> Enum.drop(3) 
        |> Enum.map(fn part -> 
          case Integer.parse(part) do
            {num, _rest} -> num
            :error -> 999
          end
        end)
      end)
      |> Enum.map(fn child -> 
        short_title = String.split(child.title, " - ", parts: 2) |> List.last()
        parts = String.split(short_title, " (")
        if length(parts) == 2 do
          base = List.first(parts) |> String.downcase() |> String.capitalize()
          "#{base} (#{List.last(parts)}"
        else
          # if it still has multiple parenthesis, just capitalize the first letter
          String.capitalize(String.slice(short_title, 0, 1)) <> String.slice(short_title, 1..-1//1)
        end
      end)

    if children_titles == [] do
      ""
    else
      Enum.join(children_titles, "; ") <> "."
    end
  end
